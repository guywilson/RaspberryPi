/*
** 100mph = 160kph = 160,000 metres/hour = 44.4 m/s
**
** If we have an anometer with a diameter of 15cm for example
** One revolution = 0.15m x pi = 0.47m, so with a 100mph wind
** it would revolve 94.3 times per second (44.4 m/s / 0.47m).
**
** So, if we had 2 magnets on our anometer blades for balance,
** our reed switch would fire at 94.3 x 2 = 188.63Hz or every
** 5ms.
*/